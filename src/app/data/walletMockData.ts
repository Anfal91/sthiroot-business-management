// Comprehensive Wallet System Mock Data
// This structure can be directly translated to database schemas

// Wallet Model - Each user (Partner/Employee/Store) has one wallet
export interface Wallet {
  id: string;
  userId: string; // Reference to partner_id, employee_id, or store_id
  userType: 'Partner' | 'Employee' | 'Store';
  userName: string;
  userCode: string;
  balance: number;
  totalTopUp: number; // Lifetime top-up amount
  totalWithdraw: number; // Lifetime withdraw amount
  totalReceived: number; // Money received from accountant/admin
  totalSpent: number; // Money spent (orders, etc.)
  status: 'Active' | 'Suspended' | 'Frozen';
  createdAt: string;
  updatedAt: string;
}

// Top-Up Request Model - Partners and Stores can request to add money
export interface TopUpRequest {
  id: string;
  requestNumber: string; // Auto-generated: TUR-001
  walletId: string;
  userId: string;
  userType: 'Partner' | 'Store';
  userName: string;
  userCode: string;
  amount: number;
  paymentScreenshot: string; // File path or URL
  transactionId: string; // User's payment transaction ID
  transactionDate: string; // Date when user made payment
  notes: string;
  status: 'Pending' | 'Approved' | 'Rejected';
  requestedAt: string;
  reviewedBy?: string; // Accountant/Admin ID
  reviewedByName?: string;
  reviewedAt?: string;
  rejectionReason?: string;
  whatsappNotified: boolean;
}

// Withdraw Request Model - All three can request withdrawal
export interface WithdrawRequest {
  id: string;
  requestNumber: string; // Auto-generated: WDR-001
  walletId: string;
  userId: string;
  userType: 'Partner' | 'Employee' | 'Store';
  userName: string;
  userCode: string;
  requestedAmount: number;
  bankAccountNumber?: string;
  ifscCode?: string;
  upiId?: string;
  notes: string;
  status: 'Pending' | 'Approved' | 'Rejected' | 'Processed';
  requestedAt: string;
  reviewedBy?: string;
  reviewedByName?: string;
  reviewedAt?: string;
  processedAt?: string; // When money was actually sent
  paymentScreenshot?: string; // Proof of payment by accountant
  paymentTransactionId?: string; // Bank/UPI transaction ID
  rejectionReason?: string;
  whatsappNotified: boolean;
}

// Transaction Model - All wallet transactions
export interface WalletTransaction {
  id: string;
  transactionNumber: string; // Auto-generated: TXN-001
  walletId: string;
  userId: string;
  userType: 'Partner' | 'Employee' | 'Store';
  userName: string;
  userCode: string;
  type: 'TopUp' | 'Withdraw' | 'Received' | 'Spent' | 'Refund' | 'Commission' | 'Reward' | 'Deduction';
  amount: number;
  balanceBefore: number;
  balanceAfter: number;
  description: string;
  referenceType?: 'TopUpRequest' | 'WithdrawRequest' | 'Order' | 'AdminTransfer' | 'Commission';
  referenceId?: string; // ID of related request/order
  performedBy?: string; // Who initiated (for admin transfers)
  performedByName?: string;
  paymentScreenshot?: string;
  transactionId?: string;
  transactionDate: string;
  notes?: string;
  status: 'Completed' | 'Pending' | 'Failed' | 'Reported';
  reportedAt?: string;
  reportReason?: string;
  reportStatus?: 'Pending' | 'Resolved' | 'Rejected';
  createdAt: string;
}

// Admin Send Money Model - When accountant sends money directly to wallets
export interface AdminMoneyTransfer {
  id: string;
  transferNumber: string; // Auto-generated: AMT-001
  recipientWalletId: string;
  recipientUserId: string;
  recipientUserType: 'Partner' | 'Employee' | 'Store';
  recipientUserName: string;
  recipientUserCode: string;
  amount: number;
  transferType: 'Reward' | 'Incentive' | 'Bonus' | 'Refund' | 'Adjustment' | 'Other';
  paymentScreenshot: string;
  transactionId: string; // Admin's payment transaction ID
  transactionDate: string;
  notes: string;
  sentBy: string; // Accountant/Admin ID
  sentByName: string;
  status: 'Completed' | 'Pending';
  transactionRecordId?: string; // Link to WalletTransaction
  createdAt: string;
}

// Reported Transaction Model
export interface ReportedTransaction {
  id: string;
  reportNumber: string; // Auto-generated: RPT-001
  transactionId: string;
  transactionNumber: string;
  walletId: string;
  userId: string;
  userType: 'Partner' | 'Employee' | 'Store';
  userName: string;
  userCode: string;
  reportReason: string;
  reportDetails: string;
  expectedAmount: number;
  reportedAt: string;
  status: 'Pending' | 'UnderReview' | 'Resolved' | 'Rejected';
  reviewedBy?: string;
  reviewedByName?: string;
  reviewedAt?: string;
  resolution?: string;
  resolutionDate?: string;
}

